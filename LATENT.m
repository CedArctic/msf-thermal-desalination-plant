	function HLV =  LATENT(TB)

   BPR=0;
	TT=(TB-273)*1.8+32;
	TT=TT-BPR;
	T=(TT-32)/1.8+273.15;
	T=TB;

	b1=2641.62/T*10^(80870/(T^2.0));
	b2=82.546;
	b3=162460/T;
	b4=0.21828*T;
	b5=126970/T;
	b0=1.89-b1;
	f0=1.89-b1*(372420/(T^2.0)+2.0);
	b6=b0*b3-2*f0*(b2-b3);
	b7=2*f0*(b4-b5)-b0*b5;
	ff=775.596+0.63296*T+0.000162467*(T^2.0);
	ff=ff+47.3635*log10(T^3.0);

	P = PRESS(T);
	HV=(f0*P+b0/2*(P/T)^2*(-b6+b0*(b2-b3+2*b7*(P/T)^2)));
	HV=ff+0.043557*HV;

	Tc=T-273.15;
	HVV=(2499.15+1.955*Tc-1.927e-3*Tc*Tc)/4.1868;

	HD=-31.92+1.0011833*TT-3.0833326e-5*TT^2;
	HD=HD+4.666663e-8*TT^3+3.333334e-10*TT^4;

	HLV=HV-HD;

	HLV=HLV*252/0.4536/1000.0;
	HLV=HVV-HD*252/0.4536/1000;
	
	